#include <stdio.h>

void	ft_swap(int *a, int *b)
{
	int c;
	
	c = *a;
	*a = *b;
	*b = c;
}

int main(void)
{
	int c;
	int d;

	scanf("%d%d", &c, &d);

	int *a = &c;
	int *b = &d;

	ft_swap(a, b);

	printf("a = %d, b = %d", *a, *b);
	return (0);
}
